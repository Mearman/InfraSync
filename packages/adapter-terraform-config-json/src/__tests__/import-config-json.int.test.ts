// ─── Round-trip fixture ──────────────────────────────────────────────────────

test("exported then re-imported IR preserves core structure", () => {
  // Build a tf.json that matches what the exporter would produce
  const tfJson = JSON.stringify({
    "//": 'Generated by InfraSync from "round-trip-test". Do not edit.',
    terraform: {
      required_providers: {
        cloudflare: { source: "cloudflare/cloudflare" },
      },
    },
    provider: {
      cloudflare: { api_token: "${var.CF_TOKEN}" },
    },
    resource: {
      cloudflare_zone: {
        main: { domain: "example.com" },
      },
      cloudflare_record: {
        www: {
          zone_id: "${cloudflare_zone.main.id}",
          type: "CNAME",
          content: "target.example.com",
          depends_on: ["cloudflare_zone.main"],
        },
      },
    },
    variable: {
      CF_TOKEN: { type: "string", sensitive: true },
    },
  });

  const result = importTfConfigJson(tfJson);

  // Name preserved from comment
  assert.equal(result.ir.name, "round-trip-test");

  // Provider preserved
  assert.equal(result.ir.providers.length, 1);
  const provider = firstProvider(result);
  assert.equal(provider.adapterName, "cloudflare");

  // Resources preserved
  assert.equal(result.ir.resources.length, 2);

  const zone = findResource(result, "main");
  assert.equal(zone.kind, "Zone");
  assert.equal(zone.mode, "manage");
  assert.equal(zone.spec.domain, "example.com");

  const record = findResource(result, "www");
  assert.equal(record.kind, "Record");
  assert.deepEqual(record.dependsOn, ["main"]);

  // Ref token preserved
  const zoneIdSpec = record.spec.zone_id;
  assert.ok(isRecord(zoneIdSpec));
  assert.ok("$ref" in zoneIdSpec);
});

